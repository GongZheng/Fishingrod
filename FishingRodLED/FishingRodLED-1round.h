void fishingrod_encrypt_rounds(const uint8_t *plain, const uint8_t *key, uint8_t *cipher)
{
    const uint64_t *rk;
    uint64_t L,R,x,t;
    int i;
    rk = (uint64_t *) key;
    L =     (plain[0 ]         ) ^ ((u64)plain[1 ]<<8  )  ^
            ((u64)plain[2 ]<<16     ) ^ ((u64)plain[3 ]<<24 )  ^
            ((u64)plain[4 ]<<32     ) ^ ((u64)plain[5 ]<<40 )  ^
            ((u64)plain[6 ]<<48     ) ^ ((u64)plain[7 ]<<56);

    R =     (plain[8 ]         ) ^ ((u64)plain[9 ]<<8  )  ^
            ((u64)plain[10]<<16     ) ^ ((u64)plain[11]<<24 )  ^
            ((u64)plain[12]<<32     ) ^ ((u64)plain[13]<<40 )  ^
            ((u64)plain[14]<<48     ) ^ ((u64)plain[15]<<56);

    for(i=0;i<ROUNDS;i++, rk++) {
        x  = (L & *rk) ^ R;
        t = Ssbox[0][ x      &0xff] ^
            Ssbox[1][(x>>8)  &0xff] ^    
            Ssbox[2][(x>>16) &0xff] ^    
            Ssbox[3][(x>>24) &0xff] ^    
            Ssbox[4][(x>>32) &0xff] ^    
            Ssbox[5][(x>>40) &0xff] ^    
            Ssbox[6][(x>>48) &0xff] ^    
            Ssbox[7][(x>>56)      ];   
        x = L; 
        L = (t & *rk) ^ R;
        R = x ^ t;
    }
    cipher[0] = L;
    cipher[1] = L>>8;
    cipher[2] = L>>16;
    cipher[3] = L>>24;
    cipher[4] = L>>32;
    cipher[5] = L>>40;
    cipher[6] = L>>48;
    cipher[7] = R>>56;

    cipher[8]  = R;
    cipher[9]  = R>>8;
    cipher[10] = R>>16;
    cipher[11] = R>>24;
    cipher[12] = R>>32;
    cipher[13] = R>>40;
    cipher[14] = R>>48;
    cipher[15] = R>>56;
}


