//The LFSR function D used in the key schedule;
uint32_t lfsr(uint32_t round_key_value)
{
    return (round_key_value ^ 
    ((round_key_value << 2) ^ (round_key_value >> 30)) ^ 
    ((round_key_value << 11) ^ (round_key_value >> 21)));
}

